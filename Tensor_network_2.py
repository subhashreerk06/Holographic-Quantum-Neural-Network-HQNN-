print()
print("  Free boundary legs (physical qubits):")
print(f"    Node 0: 4 boundary legs  (physical indices 0,1,2,3)")
print(f"    Node 1: 5 boundary legs  (physical indices 1,2,3,4,5)")
print(f"    Node 2: 5 boundary legs  (physical indices 1,2,3,4,5)")
print(f"    Total: {n_boundary} boundary qubits")
print()
